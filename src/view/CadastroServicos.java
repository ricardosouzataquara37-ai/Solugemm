    private void btnSalvarServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarServicoActionPerformed
        String strDescricao = txtDescricao.getText();
        String strPreco = txtPreco.getText();
        String strCodigo = txtCodigo.getText();
        
        if(strDescricao.trim().isEmpty() || strPreco.trim().isEmpty() || strCodigo.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchiodos");
        }
        
        if (!strPreco.matches("^[0-9]+(,[0-9]{1,2})?$")) {
             JOptionPane.showMessageDialog(this, "Formato inválido! Use formato: 0,00");
             return; 
        }
        String limpo = strPreco.replace(".", "").replace(",", ".");
         if (!strCodigo.matches("^[0-9]{4}$")) {
              JOptionPane.showMessageDialog(this,"Código Invalido, digite digite 4 caracteres de 0 a 9");
            return;
        }
        
        
        TipoDeServicoDao dao = new TipoDeServicoDao();
        TipoDeServico tds = new TipoDeServico();
        
        tds.setDescricao(strDescricao);
        try {
              tds.setPrecoServico(Double.parseDouble(limpo));
        } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ocorreu um erro na conersão do preço!");
          return;
        }
        try{
        tds.setCodigo(strCodigo);
        
        dao.salvar(tds);
        model();
        JOptionPane.showMessageDialog(this, "Serviço cadastrado com sucesso");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro inesperado, material não cadastrado");
        } 
        
    }//GEN-LAST:event_btnSalvarServicoActionPerformed

