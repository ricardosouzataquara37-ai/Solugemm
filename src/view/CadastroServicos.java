    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed

        String strDescricao = txtDescricao.getText();
        String strPreco = txtPreco.getText();
        String strCodigo = txtCodigo.getText();
      
        if(strDescricao.trim().isEmpty() || strPreco.trim().isEmpty() || strCodigo.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Todos os campos obrigatórios devem ser preenchidos");
            return;
        }

        if (!strPreco.matches("^[0-9]+(,[0-9]{1,2})?$")) {
            JOptionPane.showMessageDialog(this, "Formato inválido! Use formato: 0,00");
            return;
        }
        String limpo = strPreco.replace(".", "").replace(",", ".");
       
        if (!strCodigo.matches("^[0-9]{4}$")) {
            JOptionPane.showMessageDialog(this,"Código Invalido, digite digite 4 caracteres de 0 a 9");
            return;
        }

        TipoDeServicoDao dao = new TipoDeServicoDao();
        TipoDeServico tipoServ = new TipoDeServico();

        tipoServ.setDescricao(strDescricao);
        try {
            tipoServ.setPrecoServico(Double.parseDouble(limpo));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ocorreu um erro na conersão do preço!");
            return;
        }
        try{
            tipoServ.setCodigo(strCodigo);
            tipoServ.setIdTds(idtds);

            dao.atualizar(tipoServ);

            model();
            JOptionPane.showMessageDialog(this, "Serviço atualizado com sucesso");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro inesperado, Serviço não atualizado");
        }

    }//GEN-LAST:event_btnAtualizarActionPerformed

