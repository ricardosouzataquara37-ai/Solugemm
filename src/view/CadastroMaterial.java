    private void btnSalvarMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarMaterialActionPerformed
        String strNome = txtNome.getText();
        String strPreco = txtPreco.getText();
        String strCodigo = txtCodigo.getText();
        String strFabricante = txtFabricante.getText();
        String strCNPJ = txtCNPJ.getText().trim();
        
        if(strNome.trim().isEmpty() || strPreco.trim().isEmpty() || strCodigo.trim().isEmpty() || strFabricante.trim().isEmpty() || strCNPJ.trim().isEmpty() ){
            JOptionPane.showMessageDialog(this, "Todos os campos obrigatórios devem ser preenchidos");
            return;
        }
        
        if (!strPreco.matches("^[0-9]+(,[0-9]{1,2})?$")) {
             JOptionPane.showMessageDialog(this, "Formato inválido! Use formato: 0,00");
             return; 
        }
         String limpo = strPreco.replace(".", "").replace(",", ".");
        if (!strCNPJ.matches("^[0-9]{14}$")) {
              JOptionPane.showMessageDialog(this,"CNPJ inválido, digite 14 caracteres de 0 a 9");
            return;
        }
        if (!strCodigo.matches("^[0-9]{4}$")) {
              JOptionPane.showMessageDialog(this,"Código Invalido, digite digite 4 caracteres de 0 a 9");
            return;
        }
        
        MaterialDao dao = new MaterialDao();
        Fabricante fab = new Fabricante();
        Material mat = new Material();
        
        mat.setNome(strNome);
        try {
              mat.setPreco(Double.parseDouble(limpo));
        } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ocorreu um erro na conersão do preço!");
          return;
        }
      try{
        mat.setCodigo(strCodigo);
        
        fab.setNome(strFabricante);
        fab.setCnpj(strCNPJ);
        
        mat.setFabricante(fab);
        
        dao.salvarMaterialComFabricante(mat);
        
        model();
        JOptionPane.showMessageDialog(this, "Material cadastrado com sucesso");
      }catch(Exception e){
          JOptionPane.showMessageDialog(this, "Erro inesperado, material não cadastrado");
      }    
    }//GEN-LAST:event_btnSalvarMaterialActionPerformed

