public void model() {
    DefaultTableModel model = (DefaultTableModel) tabelaMaterial.getModel();
    MaterialDao dao = new MaterialDao();
    List<Material> lista = dao.consultarTodos();
    
   
    if (lista == null || lista.isEmpty()) {
        System.out.println("DEBUG: A lista retornada pelo DAO está vazia.");
        return; 
    }
    
    model.setRowCount(0);
    DecimalFormat df = new DecimalFormat("#,##0.00");
    for (Material m : lista) {
        
        model.addRow(new Object[]{
            m.getIdMaterial(),
            m.getCodigo(),
            m.getNome(),
            df.format(m.getPreco()),
            m.getFabricante().getNome()
        });
    }
    tabelaMaterial.getColumnModel().getColumn(0).setMinWidth(0);
    tabelaMaterial.getColumnModel().getColumn(0).setMaxWidth(0);
    tabelaMaterial.getColumnModel().getColumn(0).setPreferredWidth(0);    


}


