    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tabelaMaterial.getSelectedRow();
        MaterialDao dao = new MaterialDao();

        if (linhaSelecionada >= 0) {
            
            int idMaterial = (int) tabelaMaterial.getValueAt(linhaSelecionada, 0);
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja excluir esta consulta?");

            if (resposta == JOptionPane.YES_OPTION) {

                dao.remover(idMaterial);

                model();

                JOptionPane.showMessageDialog(this, "Item excluido com sucesso!");
            }

        } else {

            JOptionPane.showMessageDialog(this, "Selecione uma consulta na tabela para excluir.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

