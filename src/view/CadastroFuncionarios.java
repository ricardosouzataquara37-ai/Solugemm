    private void btnSalvarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarFuncionarioActionPerformed
        String nome = txtNome.getText();
        String telefone = txtTelefone.getText();
        String email = txtEmail.getText();
        String cpf = txtCPF.getText();
        String data = "";
        
         try{
            txtDataNascimento.commitEdit();
            data = txtDataNascimento.getText().trim();
        }catch(java.text.ParseException e){
            JOptionPane.showMessageDialog(null, "Digite a data correta");
        }
       
        String matricula = txtMatricula.getText();
        String sexo = txtSexo.getText();
        Empresa empresaSelecionada = (Empresa) cbmEmpresa.getSelectedItem();
      
        
        Cargo cargoSelecionado = (Cargo) cbmCargo.getSelectedItem();
        
        
        String cep = txtCEP.getText();
        String bairro = txtBairro.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
        String rua = txtRua.getText();
        String numero = txtNumero.getText();
        String casa = txtCasa.getText();
        String apartamento = txtApartamento.getText();
        String bloco = txtBloco.getText();
        
        
        
        if(nome.trim().isEmpty() || telefone.trim().isEmpty() || cep.trim().isEmpty()
        || bairro.trim().isEmpty() || cidade.trim().isEmpty()||estado.trim().isEmpty()||rua.trim().isEmpty()
        || numero.trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos");
            return;
        }
        
        if (!numero.matches("^(?i)(SN|[0-9]+[\\s-]?[A-Za-z]?)$")) {
            JOptionPane.showMessageDialog(this,"Erro! Por favor, insira um número válido (ex: 123, 123-A ou SN)");
            return;
        }
        
        if (!telefone.matches("^[0-9]{10,11}$")) {
             JOptionPane.showMessageDialog(this, "O telefone deve conter apenas números (DDD + número) e ter entre 10 e 11 dígitos.");
             return;
        } 
        if (!data.matches("^(0[1-9]|[12][0-9]|3[01])/(0[1-9]|1[0-2])/[0-9]{4}$")) {
            JOptionPane.showMessageDialog(this,"Data inválida");
            return;
        }
        if (!cpf.matches("^[0-9]{11}$")) {
              JOptionPane.showMessageDialog(this,"CPF inválido");
              return;
        }
        if (!cep.matches("^[0-9]{5}-?[0-9]{3}$")) {
           JOptionPane.showMessageDialog(this, "CEP inválido! Use o formato 00000-000.");
           return;
        }
        if (!matricula.matches("^[0-9]{4}$")) {
          JOptionPane.showMessageDialog(this, "A matrícula deve conter exatamente 4 números!");
          return;
        }
        if (!sexo.matches("^[MmFf]$")) {
          JOptionPane.showMessageDialog(this, "Sexo inválido! Digite apenas M ou F.");
          return;
        }       
     try{   
        Funcionario func = new Funcionario();
        Endereco endereco = new Endereco();
        Telefone telefon = new Telefone();
        FuncionarioDao dao = new FuncionarioDao();
        
        endereco.setCep(cep);
        endereco.setBairro(bairro);
        endereco.setCidade(cidade);
        endereco.setEstado(estado);
        endereco.setRua(rua);
        endereco.setNumero(numero);
        endereco.setCasa(casa);
        endereco.setNumeroApartamento(apartamento);
        endereco.setBloco(bloco);
        
        telefon.setNumero(telefone);
        
        func.setNome(nome);
        func.setTelefome(telefon);
        func.setEmail(email);
        func.setIdentificacao(cpf);
        DateTimeFormatter formatadorBR = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dataNascimento = LocalDate.parse(data,formatadorBR);
        func.setDataNascimento(dataNascimento);
        func.setMatricula(matricula);
        func.setSexo(sexo);
        func.setEmpresa(empresaSelecionada);
        func.setCargo(cargoSelecionado);
        func.setEndereco(endereco);
        
        dao.salvar(func);
        model3();
        JOptionPane.showMessageDialog(this, "Funcionário cadastrado com sucesso");
     }catch(Exception e){
         JOptionPane.showMessageDialog(this, "Erro inesperado, cadastro não realizado");
     }    
       // -------------------------------------------------
     cbmCargo.setEnabled(false);
    }//GEN-LAST:event_btnSalvarFuncionarioActionPerformed

