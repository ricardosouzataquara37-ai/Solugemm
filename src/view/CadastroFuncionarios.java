 public void carregarComboCargo(){
     CargoDao dao = new CargoDao();
     List<Cargo> lista = dao.listarTodos();
     cbmCargo.removeAllItems();
     DefaultComboBoxModel model = new DefaultComboBoxModel<>();
     for(Cargo c: lista){
         model.addElement(c);
     }
   
     cbmCargo.setModel((DefaultComboBoxModel) model);
 }
