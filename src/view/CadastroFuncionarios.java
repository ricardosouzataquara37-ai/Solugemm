 public void model3() {
    DefaultTableModel model = (DefaultTableModel) tabelaFuncionarios.getModel();
    FuncionarioDao dao = new FuncionarioDao();
    List<Funcionario> lista = dao.consultarTodos();
    
   
    if (lista == null || lista.isEmpty()) {
        System.out.println("DEBUG: A lista retornada pelo DAO está vazia.");
        return; 
    }
    
    model.setRowCount(0);
    
    for (Funcionario f : lista) {
        // Proteção contra valores nulos nas colunas
        model.addRow(new Object[]{
            f.getMatricula(),
            f.getNome(),
            (f.getCargo() != null) ? f.getCargo().toString() : "-",
            (f.getEmpresa() != null) ? f.getEmpresa().toString() : "-"
        });
    }
}
 

