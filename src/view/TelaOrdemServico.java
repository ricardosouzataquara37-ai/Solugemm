    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String strCEP = txtCEPOs.getText();
        String strBairro = txtBairroOs.getText();
        String strCidade = txtCidadeOs.getText();
        String strEstado = txtEstadoOs.getText();
        String strRua = txtRuaOs.getText();
        String strNumero = txtNumero.getText();
        String strCasa = txtCasa.getText();
        String strApartamento = txtApartamento.getText();
        String strBloco = txtBloco.getText();
        String strData = "";
        
        if(strCEP.trim().isEmpty() || strBairro.trim().isEmpty()||
        strCidade.trim().isEmpty()||strEstado.trim().isEmpty()||
        strRua.trim().isEmpty()||strNumero.trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos");
        }
        
        try{
            txtDataOs.commitEdit();
            strData = txtDataOs.getText().trim();
        }catch(java.text.ParseException e){
            JOptionPane.showMessageDialog(null, "Digite a data correta");
        }
        
        if (!strNumero.matches("^(?i)(SN|[0-9]+[\\s-]?[A-Za-z]?)$")) {
            JOptionPane.showMessageDialog(this,"Erro! Por favor, insira um número válido (ex: 123, 123-A ou SN)");
            return;
        }
        
       
        if (!strData.matches("^(0[1-9]|[12][0-9]|3[01])/(0[1-9]|1[0-2])/[0-9]{4}$")) {
            JOptionPane.showMessageDialog(this,"Data inválida");
            return;
        }
        
        if (!strCEP.matches("^[0-9]{5}-?[0-9]{3}$")) {
           JOptionPane.showMessageDialog(this, "CEP inválido! Use o formato 00000-000.");
           return;
        }
       
        JOptionPane.showMessageDialog(this, "Dados salvo com sucesso");
        validarEnderecoOs = true;
     
    }//GEN-LAST:event_jButton1ActionPerformed

